    private void btn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SalvarActionPerformed

        
        
        
        String nome = txt_Nome.getText();
        String cpf = txt_Cpf.getText().replaceAll("[./-]", "");
        String sexo = cbo_Sexo.getSelectedItem().toString();
        String telFixo = txt_TelFixo.getText().replaceAll("[./-]", "");
        

        if (nome.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe o nome");
        } else if (cpf.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe o CPF");
        } else if (telFixo.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Informe o telefone");
        } else {

            
            DaoClientes daoClientes = new DaoClientes();
            
            if(daoClientes.verificaDuplicidade(cpf)){
                
                JOptionPane.showMessageDialog(rootPane, "CPF j√° cadastrado");
                
            }
            else{
            
            Cliente cliente = new Cliente(nome, cpf, sexo, telFixo);

            daoClientes.salvarCliente(cliente);

            JOptionPane.showMessageDialog(rootPane, "Dados salvos com sucesso !");

            txt_Nome.setText(null);
            txt_Cpf.setText(null);
            txt_TelFixo.setText(null);
            }
        }


    }//GEN-LAST:event_btn_SalvarActionPerformed

